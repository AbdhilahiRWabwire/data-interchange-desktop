// Inch Conversion

// Centimeters to Inches
pub fn centimetersToInches(centimeters: f64) f64 {
    return centimeters * 0.3937008;
}

// Decameters to Inches
pub fn decametersToInches(decameters: f64) f64 {
    return decameters * 393.7008;
}

// Decimeters to Inches
pub fn decimetersToInches(decimeters: f64) f64 {
    return decimeters * 3.937008;
}

// Feet to Inches
pub fn feetToInches(feet: f64) f64 {
    return feet * 12;
}

// Hectometers to Inches
pub fn hectometersToInches(hectometers: f64) f64 {
    return hectometers * 3937.008;
}

// Kilometers to Inches
pub fn kilometersToInches(kilometers: f64) f64 {
    return kilometers * 39370.08;
}

// Meters to Inches
pub fn metersToInches(meters: f64) f64 {
    return meters * 39.37008;
}

// Micrometers to Inches
pub fn micrometersToInches(micrometers: f64) f64 {
    return micrometers * 0.00003937008;
}

// Miles to Inches
pub fn milesToInches(miles: f64) f64 {
    return miles * 63360;
}

// Millimeters to Inches
pub fn millimetersToInches(millimeters: f64) f64 {
    return millimeters * 0.03937008;
}

// Nanometers to Inches
pub fn nanometersToInches(nanometers: f64) f64 {
    return nanometers * 0.00000003937008;
}

// Nautical Miles to Inches
pub fn nauticalMilesToInches(nauticalMiles: f64) f64 {
    return nauticalMiles * 72913.42;
}

// Picometers to Inches
pub fn picometersToInches(picometers: f64) f64 {
    return picometers * 0.00000000003937008;
}

// Yards to Inches
pub fn yardsToInches(yards: f64) f64 {
    return yards * 36;
}