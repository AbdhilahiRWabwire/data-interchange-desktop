// Astronaumical Unit Conversion

// Kilometers to Astronaumical Units
double kilometersToAstronaumicalUnits(double kilometers) {
  return kilometers * 0.000000006684587;
}

// Light Years to Astronaumical Units
double lightYearsToAstronaumicalUnits(double lightYears) {
  return lightYears * 63241.08;
}

// Miles to Astronaumical Units
double milesToAstronaumicalUnits(double miles) {
  return miles * 0.0000000107578;
}

// Nautical Miles to Astronaumical Units
double nauticalMilesToAstronaumicalUnits(double nauticalMiles) {
  return nauticalMiles * 0.00000001237986;
}

// Parsecs to Astronaumical Units
double parsecsToAstronaumicalUnits(double parsecs) {
  return parsecs * 206264.8;
}
