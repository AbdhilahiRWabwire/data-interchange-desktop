// Meter Conversion

// Centimeters to Meters
double centimetersToMeters(double centimeters) {
  return centimeters * 0.01;
}

// Decameters to Meters
double decametersToMeters(double decameters) {
  return decameters * 10;
}

// Decimeters to Meters
double decimetersToMeters(double decimeters) {
  return decimeters * 0.1;
}

// Feet to Meters
double feetToMeters(double feet) {
  return feet * 0.3048;
}

// Hectometers to Meters
double hectometersToMeters(double hectometers) {
  return hectometers * 100;
}

// Inches to Meters
double inchesToMeters(double inches) {
  return inches * 0.0254;
}

// Kilometers to Meters
double kilometersToMeters(double kilometers) {
  return kilometers * 1000;
}

// Micrometers to Meters
double micrometersToMeters(double micrometers) {
  return micrometers * -1000000;
}

// Miles to Meters
double milesToMeters(double miles) {
  return miles * 1609.344;
}

// Millimeters to Meters
double millimetersToMeters(double millimeters) {
  return millimeters * 0.001;
}

// Nanometers to Meters
double nanometersToMeters(double nanometers) {
  return nanometers * -1000000000;
}

// Nautical Miles to Meters
double nauticalMilesToMeters(double nauticalMiles) {
  return nauticalMiles * 1852.001;
}

// Picometers to Meters
double picometersToMeters(double picometers) {
  return picometers * -1000000000000;
}

// Yards to Meters
double yardsToMeters(double yards) {
  return yards * 0.9144;
}
