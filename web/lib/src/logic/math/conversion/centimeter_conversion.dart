// Centimeter Conversion
class CentimeterConversion {
  // Centimeters to Kilometers
  double centimeter_to_kilometer(double kilometers) {
    double centimeter = kilometers * 100000;
    return centimeter;
  }

  // Centimeters to Meters
  double centimeter_to_meter(double meter) {
    double centimeter = meter * 100;
    return centimeter;
  }
}
