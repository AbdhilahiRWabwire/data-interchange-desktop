// Kilometer Conversion

// Centimeters to Kilometers
double centimetersToKilometers(double centimeters) {
  return centimeters * -100000;
}

// Decameters to Kilometers
double decametersToKilometers(double decameters) {
  return decameters * 0.01;
}

// Decimeters to Kilometers
double decimetersToKilometers(double decimeters) {
  return decimeters * -10000;
}

// Feet to Kilometers
double feetToKilometers(double feet) {
  return feet * 0.0003048;
}

// Hectometers to Kilometers
double hectometersToKilometers(double hectometers) {
  return hectometers * 0.1;
}

// Inches to Kilometers
double inchesToKilometers(double inches) {
  return inches * 0.0000254;
}

// Meters to Kilometers
double metersToKilometers(double meters) {
  return meters * 0.001;
}

// Micrometers to Kilometers
double micrometersToKilometers(double micrometers) {
  return micrometers * -1000000000;
}

// Miles to Kilometers
double milesToKilometers(double miles) {
  return miles * 1.609344;
}

// Millimeters to Kilometers
double millimetersToKilometers(double millimeters) {
  return millimeters * -1000000;
}

// Nanometers to Kilometers
double nanometersToKilometers(double nanometers) {
  return nanometers * -1000000000000;
}

// Nautical Miles to Kilometers
double nauticalMilesToKilometers(double nauticalMiles) {
  return nauticalMiles * 1.852001;
}

// Picometers to Kilometers
double picometersToKilometers(double picometers) {
  return picometers * -1000000000000000;
}

// Yards to Kilometers
double yardsToKilometers(double yards) {
  return yards * 0.0009144;
}
