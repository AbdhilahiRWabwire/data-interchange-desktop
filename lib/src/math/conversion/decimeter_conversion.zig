// Decimeter Conversion

// Centimeters to Decimeters
pub fn centimetersToDecimeters(centimeters: f64 ) f64  {
    return centimeters * 0.1;
}

// Decameters to Decimeters
pub fn decametersToDecimeters(decameters: f64 ) f64  {
    return decameters * 100;
}

// Feet to Decimeters
pub fn feetToDecimeters(feet: f64 ) f64  {
    return feet * 3.048;
}

// Hectometers to Decimeters
pub fn hectometersToDecimeters(hectometers: f64 ) f64  {
    return hectometers * 1000;
}

// Inches to Decimeters
pub fn inchesToDecimeters(inches: f64 ) f64  {
    return inches * 0.254;
}

// Kilometers to Decimeters
pub fn kilometersToDecimeters(kilometers: f64 ) f64  {
    return kilometers * 10000;
}

// Meters to Decimeters
pub fn metersToDecimeters(meters: f64 ) f64  {
    return meters * 10;
}

// Micrometers to Decimeters
pub fn micrometersToDecimeters(micrometers: f64 ) f64  {
    return micrometers * -100000;
}

// Miles to Decimeters
pub fn milesToDecimeters(miles: f64 ) f64  {
    return miles * 16093.44;
}

// Millimeters to Decimeters
pub fn millimetersToDecimeters(millimeters: f64 ) f64  {
    return millimeters * 0.01;
}

// Nanometers to Decimeters
pub fn nanometersToDecimeters(nanometers: f64 ) f64  {
    return nanometers * -100000000;
}

// Nautical Miles to Decimeters
pub fn nauticalMilesToDecimeters(nauticalMiles: f64 ) f64  {
    return nauticalMiles * 18520.01;
}

// Picometers to Decimeters
pub fn picometersToDecimeters(picometers: f64 ) f64  {
    return picometers * -100000000000;
}

// Yards to Decimeters
pub fn yardsToDecimeters(yards: f64 ) f64  {
    return yards * 9.144;
}