// Nanometer Conversion

// Centimeters to Nanometers
double centimetersToNanometers(double centimeters) {
  return centimeters * 10000000;
}

// Decameters to Nanometers
double decametersToNanometers(double decameters) {
  return decameters * 10000000000;
}

// Decimeters to Nanometers
double decimetersToNanometers(double decimeters) {
  return decimeters * 100000000;
}

// Feet to Nanometers
double feetToNanometers(double feet) {
  return feet * 304800000;
}

// Hectometers to Nanometers
double hectometersToNanometers(double hectometers) {
  return hectometers * 100000000000;
}

// Inches to Nanometers
double inchesToNanometers(double inches) {
  return inches * 25400000;
}

// Kilometers to Nanometers
double kilometersToNanometers(double kilometers) {
  return kilometers * 1000000000000;
}

// Meters to Nanometers
double metersToNanometers(double meters) {
  return meters * 1000000000;
}

// Micrometers to Nanometers
double micrometersToNanometers(double micrometers) {
  return micrometers * 1000;
}

// Miles to Nanometers
double milesToNanometers(double miles) {
  return miles * 1609344000000;
}

// Millimeters to Nanometers
double millimetersToNanometers(double millimeters) {
  return millimeters * 1000000;
}

// Nautical Miles to Nanometers
double nauticalMilesToNanometers(double nauticalMiles) {
  return nauticalMiles * 1852001000000;
}

// Picometers to Nanometers
double picometersToNanometers(double picometers) {
  return picometers * 0.001;
}

// Yards to Nanometers
double yardsToNanometers(double yards) {
  return yards * 914400000;
}
