// Picometer Conversion

// Centimeters to Picometers
pub fn centimetersToPicometers(centimeters: f64) f64 {
    return centimeters * 10000000000;
}

// Decameters to Picometers
pub fn decametersToPicometers(decameters: f64) f64 {
    return decameters * 10000000000000;
}

// Decimeters to Picometers
pub fn decimetersToPicometers(decimeters: f64) f64 {
    return decimeters * 100000000000;
}

// Feet to Picometers
pub fn feetToPicometers(feet: f64) f64 {
    return feet * 304800000000;
}

// Hectometers to Picometers
pub fn hectometersToPicometers(hectometers: f64) f64 {
    return hectometers * 100000000000000;
}

// Inches to Picometers
pub fn inchesToPicometers(inches: f64) f64 {
    return inches * 25400000000;
}

// Kilometers to Picometers
pub fn kilometersToPicometers(kilometers: f64) f64 {
    return kilometers * 1000000000000000;
}

// Meters to Picometers
pub fn metersToPicometers(meters: f64) f64 {
    return meters * 1000000000000;
}

// Micrometers to Picometers
pub fn micrometersToPicometers(micrometers: f64) f64 {
    return micrometers * 1000000;
}

// Miles to Picometers
pub fn milesToPicometers(miles: f64) f64 {
    return miles * 1609344000000000;
}

// Millimeters to Picometers
pub fn millimetersToPicometers(millimeters: f64) f64 {
    return millimeters * 1000000000;
}

// Nanometers to Picometers
pub fn nanometersToPicometers(nanometers: f64) f64 {
    return nanometers * 1000;
}

// Nautical Miles to Picometers
pub fn nauticalMilesToPicometers(nauticalMiles: f64) f64 {
    return nauticalMiles * 1852001000000000;
}

// Yards to Picometers
pub fn yardsToPicometers(yards: f64) f64 {
    return yards * 914400000000;
}