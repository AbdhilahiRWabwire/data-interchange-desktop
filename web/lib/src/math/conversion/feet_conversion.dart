// Feet Conversion

// Centimeters to Feet
double centimetersToFeet(double centimeters) {
  return centimeters * 0.0328084;
}

// Decameters to Feet
double decametersToFeet(double decameters) {
  return decameters * 32.8084;
}

// Decimeters to Feet
double decimetersToFeet(double decimeters) {
  return decimeters * 0.328084;
}

// Hectometers to Feet
double hectometersToFeet(double hectometers) {
  return hectometers * 328.084;
}

// Inches to Feet
double inchesToFeet(double inches) {
  return inches * 0.08333333;
}

// Kilometers to Feet
double kilometersToFeet(double kilometers) {
  return kilometers * 3280.84;
}

// Meters to Feet
double metersToFeet(double meters) {
  return meters * 3.28084;
}

// Micrometers to Feet
double micrometersToFeet(double micrometers) {
  return micrometers * 0.00000328084;
}

// Miles to Feet
double milesToFeet(double miles) {
  return miles * 5280;
}

// Millimeters to Feet
double millimetersToFeet(double millimeters) {
  return millimeters * 0.00328084;
}

// Nanometers to Feet
double nanometersToFeet(double nanometers) {
  return nanometers * 0.00000000328084;
}

// Nautical Miles to Feet
double nauticalMilesToFeet(double nauticalMiles) {
  return nauticalMiles * 6076.118;
}

// Picometers to Feet
double picometersToFeet(double picometers) {
  return picometers * 0.00000000000328084;
}

// Yards to Feet
double yardsToFeet(double yards) {
  return yards * 3;
}
