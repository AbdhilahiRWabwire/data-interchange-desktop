// Decameter Conversion

// Centimeters to Decameters
pub fn centimetersToDecameters(centimeters: f64) f64 {
    return centimeters * 0.001;
}

// Decimeters to Decameters
pub fn decimetersToDecameters(decimeters: f64) f64 {
    return decimeters * 0.01;
}

// Feet to Decameters
pub fn feetToDecameters(feet: f64) f64 {
    return feet * 0.03048;
}

// Hectometers to Decameters
pub fn hectometersToDecameters(hectometers: f64) f64 {
    return hectometers * 10;
}

// Inches to Decameters
pub fn inchesToDecameters(inches: f64) f64 {
    return inches * 0.00254;
}

// Kilometers to Decameters
pub fn kilometersToDecameters(kilometers: f64) f64 {
    return kilometers * 100;
}

// Meters to Decameters
pub fn metersToDecameters(meters: f64) f64 {
    return meters * 0.1;
}

// Micrometers to Decameters
pub fn micrometersToDecameters(micrometers: f64) f64 {
    return micrometers * -10000000;
}

// Miles to Decameters
pub fn milesToDecameters(miles: f64) f64 {
    return miles * 160.9344;
}

// Millimeters to Decameters
pub fn millimetersToDecameters(millimeters: f64) f64 {
    return millimeters * -10000;
}

// Nanometers to Decameters
pub fn nanometersToDecameters(nanometers: f64) f64 {
    return nanometers * -10000000000;
}

// Nautical Miles to Decameters
pub fn nauticalMilesToDecameters(nauticalMiles: f64) f64 {
    return nauticalMiles * 185.2001;
}

// Picometers to Decameters
pub fn picometersToDecameters(picometers: f64) f64 {
    return picometers * -10000000000000;
}

// Yards to Decameters
pub fn yardsToDecameters(yards: f64) f64 {
    return yards * 0.09144;
}