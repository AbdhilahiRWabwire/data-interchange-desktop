// Inch Conversion

// Centimeters to Inches
double centimetersToInches(double centimeters) {
  return centimeters * 0.3937008;
}

// Decameters to Inches
double decametersToInches(double decameters) {
  return decameters * 393.7008;
}

// Decimeters to Inches
double decimetersToInches(double decimeters) {
  return decimeters * 3.937008;
}

// Feet to Inches
double feetToInches(double feet) {
  return feet * 12;
}

// Hectometers to Inches
double hectometersToInches(double hectometers) {
  return hectometers * 3937.008;
}

// Kilometers to Inches
double kilometersToInches(double kilometers) {
  return kilometers * 39370.08;
}

// Meters to Inches
double metersToInches(double meters) {
  return meters * 39.37008;
}

// Micrometers to Inches
double micrometersToInches(double micrometers) {
  return micrometers * 0.00003937008;
}

// Miles to Inches
double milesToInches(double miles) {
  return miles * 63360;
}

// Millimeters to Inches
double millimetersToInches(double millimeters) {
  return millimeters * 0.03937008;
}

// Nanometers to Inches
double nanometersToInches(double nanometers) {
  return nanometers * 0.00000003937008;
}

// Nautical Miles to Inches
double nauticalMilesToInches(double nauticalMiles) {
  return nauticalMiles * 72913.42;
}

// Picometers to Inches
double picometersToInches(double picometers) {
  return picometers * 0.00000000003937008;
}

// Yards to Inches
double yardsToInches(double yards) {
  return yards * 36;
}
