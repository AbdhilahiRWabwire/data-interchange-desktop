// Meter Conversion

// Centimeters to Meters
pub fn centimetersToMeters(centimeters: f64) f64 {
    return centimeters * 0.01;
}

// Decameters to Meters
pub fn decametersToMeters(decameters: f64) f64 {
    return decameters * 10;
}

// Decimeters to Meters
pub fn decimetersToMeters(decimeters: f64) f64 {
    return decimeters * 0.1;
}

// Feet to Meters
pub fn feetToMeters(feet: f64) f64 {
    return feet * 0.3048;
}

// Hectometers to Meters
pub fn hectometersToMeters(hectometers: f64) f64 {
    return hectometers * 100;
}

// Inches to Meters
pub fn inchesToMeters(inches: f64) f64 {
    return inches * 0.0254;
}

// Kilometers to Meters
pub fn kilometersToMeters(kilometers: f64) f64 {
    return kilometers * 1000;
}

// Micrometers to Meters
pub fn micrometersToMeters(micrometers: f64) f64 {
    return micrometers * -1000000;
}

// Miles to Meters
pub fn milesToMeters(miles: f64) f64 {
    return miles * 1609.344;
}

// Millimeters to Meters
pub fn millimetersToMeters(millimeters: f64) f64 {
    return millimeters * 0.001;
}

// Nanometers to Meters
pub fn nanometersToMeters(nanometers: f64) f64 {
    return nanometers * -1000000000;
}

// Nautical Miles to Meters
pub fn nauticalMilesToMeters(nauticalMiles: f64) f64 {
    return nauticalMiles * 1852.001;
}

// Picometers to Meters
pub fn picometersToMeters(picometers: f64) f64 {
    return picometers * -1000000000000;
}

// Yards to Meters
pub fn yardsToMeters(yards: f64) f64 {
    return yards * 0.9144;
}