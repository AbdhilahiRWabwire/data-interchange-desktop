// Parsec Conversion

// Astronaumical Units to Parsecs
pub fn astronaumicalUnitsToParsecs(astronaumicalUnits: f64) f64 {
    return astronaumicalUnits * 0.000004848137;
}

// Kilometers to Parsecs
pub fn kilometersToParsecs(kilometers: f64) f64 {
    return kilometers * 0.00000000000003240779;
}

// Light Years to Parsecs
pub fn lightYearsToParsecs(lightYears: f64) f64 {
    return lightYears * 0.3066014;
}

// Miles to Parsecs
pub fn milesToParsecs(miles: f64) f64 {
    return miles * 0.00000000000005215529;
}

// Nautical Miles to Parsecs
pub fn nauticalMilesToParsecs(nauticalMiles: f64) f64 {
    return nauticalMiles * 0.00000000000006001926;
}