// Hectometer Conversion

// Centimeters to Hectometers
double centimetersToHectometers(double centimeters) {
  return centimeters * -10000;
}

// Decameters to Hectometers
double decametersToHectometers(double decameters) {
  return decameters * 0.1;
}

// Decimeters to Hectometers
double decimetersToHectometers(double decimeters) {
  return decimeters * 0.001;
}

// Feet to Hectometers
double feetToHectometers(double feet) {
  return feet * 0.003048;
}

// Inches to Hectometers
double inchesToHectometers(double inches) {
  return inches * 0.000254;
}

// Kilometers to Hectometers
double kilometersToHectometers(double kilometers) {
  return kilometers * 10;
}

// Meters to Hectometers
double metersToHectometers(double meters) {
  return meters * 0.01;
}

// Micrometers to Hectometers
double micrometersToHectometers(double micrometers) {
  return micrometers * -100000000;
}

// Miles to Hectometers
double milesToHectometers(double miles) {
  return miles * 16.09344;
}

// Millimeters to Hectometers
double millimetersToHectometers(double millimeters) {
  return millimeters * -100000;
}

// Nanometers to Hectometers
double nanometersToHectometers(double nanometers) {
  return nanometers * -100000000000;
}

// Nautical Miles to Hectometers
double nauticalMilesToHectometers(double nauticalMiles) {
  return nauticalMiles * 18.52001;
}

// Picometers to Hectometers
double picometersToHectometers(double picometers) {
  return picometers * -100000000000000;
}

// Yards to Hectometers
double yardsToHectometers(double yards) {
  return yards * 0.009144;
}
