// Decimeter Conversion

// Centimeters to Decimeters
double centimetersToDecimeters(double centimeters) {
  return centimeters * 0.1;
}

// Decameters to Decimeters
double decametersToDecimeters(double decameters) {
  return decameters * 100;
}

// Feet to Decimeters
double feetToDecimeters(double feet) {
  return feet * 3.048;
}

// Hectometers to Decimeters
double hectometersToDecimeters(double hectometers) {
  return hectometers * 1000;
}

// Inches to Decimeters
double inchesToDecimeters(double inches) {
  return inches * 0.254;
}

// Kilometers to Decimeters
double kilometersToDecimeters(double kilometers) {
  return kilometers * 10000;
}

// Meters to Decimeters
double metersToDecimeters(double meters) {
  return meters * 10;
}

// Micrometers to Decimeters
double micrometersToDecimeters(double micrometers) {
  return micrometers * -100000;
}

// Miles to Decimeters
double milesToDecimeters(double miles) {
  return miles * 16093.44;
}

// Millimeters to Decimeters
double millimetersToDecimeters(double millimeters) {
  return millimeters * 0.01;
}

// Nanometers to Decimeters
double nanometersToDecimeters(double nanometers) {
  return nanometers * -100000000;
}

// Nautical Miles to Decimeters
double nauticalMilesToDecimeters(double nauticalMiles) {
  return nauticalMiles * 18520.01;
}

// Picometers to Decimeters
double picometersToDecimeters(double picometers) {
  return picometers * -100000000000;
}

// Yards to Decimeters
double yardsToDecimeters(double yards) {
  return yards * 9.144;
}
