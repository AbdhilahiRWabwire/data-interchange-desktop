// Millimeter Conversion

// Centimeters to Millimeters
double centimetersToMillimeters(double centimeters) {
  return centimeters * 10;
}

// Decameters to Millimeters
double decametersToMillimeters(double decameters) {
  return decameters * 10000;
}

// Decimeters to Millimeters
double decimetersToMillimeters(double decimeters) {
  return decimeters * 100;
}

// Feet to Millimeters
double feetToMillimeters(double feet) {
  return feet * 304.8;
}

// Hectometers to Millimeters
double hectometersToMillimeters(double hectometers) {
  return hectometers * 100000;
}

// Inches to Millimeters
double inchesToMillimeters(double inches) {
  return inches * 25.4;
}

// Kilometers to Millimeters
double kilometersToMillimeters(double kilometers) {
  return kilometers * 1000000;
}

// Meters to Millimeters
double metersToMillimeters(double meters) {
  return meters * 1000;
}

// Micrometers to Millimeters
double micrometersToMillimeters(double micrometers) {
  return micrometers * 0.001;
}

// Miles to Millimeters
double milesToMillimeters(double miles) {
  return miles * 1609344;
}

// Nanometers to Millimeters
double nanometersToMillimeters(double nanometers) {
  return nanometers * -1000000;
}

// Nautical Miles to Millimeters
double nauticalMilesToMillimeters(double nauticalMiles) {
  return nauticalMiles * 1852001;
}

// Picometers to Millimeters
double picometersToMillimeters(double picometers) {
  return picometers * -1000000000;
}

// Yards to Millimeters
double yardsToMillimeters(double yards) {
  return yards * 914.4;
}
