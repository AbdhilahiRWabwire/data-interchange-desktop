// Hectometer Conversion

// Centimeters to Hectometers
pub fn centimetersToHectometers(centimeters: f64) f64 {
    return centimeters * -10000;
}

// Decameters to Hectometers
pub fn decametersToHectometers(decameters: f64) f64 {
    return decameters * 0.1;
}

// Decimeters to Hectometers
pub fn decimetersToHectometers(decimeters: f64) f64 {
    return decimeters * 0.001;
}

// Feet to Hectometers
pub fn feetToHectometers(feet: f64) f64 {
    return feet * 0.003048;
}

// Inches to Hectometers
pub fn inchesToHectometers(inches: f64) f64 {
    return inches * 0.000254;
}

// Kilometers to Hectometers
pub fn kilometersToHectometers(kilometers: f64) f64 {
    return kilometers * 10;
}

// Meters to Hectometers
pub fn metersToHectometers(meters: f64) f64 {
    return meters * 0.01;
}

// Micrometers to Hectometers
pub fn micrometersToHectometers(micrometers: f64) f64 {
    return micrometers * -100000000;
}

// Miles to Hectometers
pub fn milesToHectometers(miles: f64) f64 {
    return miles * 16.09344;
}

// Millimeters to Hectometers
pub fn millimetersToHectometers(millimeters: f64) f64 {
    return millimeters * -100000;
}

// Nanometers to Hectometers
pub fn nanometersToHectometers(nanometers: f64) f64 {
    return nanometers * -100000000000;
}

// Nautical Miles to Hectometers
pub fn nauticalMilesToHectometers(nauticalMiles: f64) f64 {
    return nauticalMiles * 18.52001;
}

// Picometers to Hectometers
pub fn picometersToHectometers(picometers: f64) f64 {
    return picometers * -100000000000000;
}

// Yards to Hectometers
pub fn yardsToHectometers(yards: f64) f64 {
    return yards * 0.009144;
}