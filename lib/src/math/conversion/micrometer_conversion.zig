// Micrometer Conversion

// Centimeters to Micrometers
pub fn centimetersToMicrometers(centimeters: f64) f64 {
    return centimeters * 10000;
}

// Decameters to Micrometers
pub fn decametersToMicrometers(decameters: f64) f64 {
    return decameters * 10000000;
}

// Decimeters to Micrometers
pub fn decimetersToMicrometers(decimeters: f64) f64 {
    return decimeters * 100000;
}

// Feet to Micrometers
pub fn feetToMicrometers(feet: f64) f64 {
    return feet * 304800;
}

// Hectometers to Micrometers
pub fn hectometersToMicrometers(hectometers: f64) f64 {
    return hectometers * 100000000;
}

// Inches to Micrometers
pub fn inchesToMicrometers(inches: f64) f64 {
    return inches * 25400;
}

// Kilometers to Micrometers
pub fn kilometersToMicrometers(kilometers: f64) f64 {
    return kilometers * 1000000000;
}

// Meters to Micrometers
pub fn metersToMicrometers(meters: f64) f64 {
    return meters * 1000000;
}

// Miles to Micrometers
pub fn milesToMicrometers(miles: f64) f64 {
    return miles * 1609344000;
}

// Millimeters to Micrometers
pub fn millimetersToMicrometers(millimeters: f64) f64 {
    return millimeters * 1000;
}

// Nanometers to Micrometers
pub fn nanometersToMicrometers(nanometers: f64) f64 {
    return nanometers * 0.001;
}

// Nautical Miles to Micrometers
pub fn nauticalMilesToMicrometers(nauticalMiles: f64) f64 {
    return nauticalMiles * 1852001000;
}

// Picometers to Micrometers
pub fn picometersToMicrometers(picometers: f64) f64 {
    return picometers * 0.000001;
}

// Yards to Micrometers
pub fn yardsToMicrometers(yards: f64) f64 {
    return yards * 914400;
}