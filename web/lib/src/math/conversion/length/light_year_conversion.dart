// Light Year Conversion

// Astronaumical Units to Light Years
double astronaumicalUnitsToLightYears(double astronaumicalUnits) {
  return astronaumicalUnits * 0.00001581251;
}

// Kilometers to Light Years
double kilometersToLightYears(double kilometers) {
  return kilometers * 0.0000000000001057001;
}

// Miles to Light Years
double milesToLightYears(double miles) {
  return miles * 0.0000000000001701078;
}

// Nautical Miles to Light Years
double nauticalMilesToLightYears(double nauticalMiles) {
  return nauticalMiles * 0.0000000000001957566;
}

// Parsecs to Light Years
double parsecsToLightYears(double parsecs) {
  return parsecs * 3.261564;
}
