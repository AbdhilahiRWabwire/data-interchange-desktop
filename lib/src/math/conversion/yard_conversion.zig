// Yard Conversion

// Centimeters to Yards
pub fn centimetersToYards(centimeters: f64) f64 {
    return centimeters * 0.01093613;
}

// Decameters to Yards
pub fn decametersToYards(decameters: f64) f64 {
    return decameters * 10.93613;
}

// Decimeters to Yards
pub fn decimetersToYards(decimeters: f64) f64 {
    return decimeters * 0.1093613;
}

// Feet to Yards
pub fn feetToYards(feet: f64) f64 {
    return feet * 0.3333333;
}

// Hectometers to Yards
pub fn hectometersToYards(hectometers: f64) f64 {
    return hectometers * 109.3613;
}

// Inches to Yards
pub fn inchesToYards(inches: f64) f64 {
    return inches * 0.02777778;
}

// Kilometers to Yards
pub fn kilometersToYards(kilometers: f64) f64 {
    return kilometers * 1093.613;
}

// Meters to Yards
pub fn metersToYards(meters: f64) f64 {
    return meters * 1.093613;
}

// Micrometers to Yards
pub fn micrometersToYards(micrometers: f64) f64 {
    return micrometers * 0.000001093613;
}

// Miles to Yards
pub fn milesToYards(miles: f64) f64 {
    return miles * 1760;
}

// Millimeters to Yards
pub fn millimetersToYards(millimeters: f64) f64 {
    return millimeters * 0.001093613;
}

// Nanometers to Yards
pub fn nanometersToYards(nanometers: f64) f64 {
    return nanometers * 0.000000001093613;
}

// Nautical Miles to Yards
pub fn nauticalMilesToYards(nauticalMiles: f64) f64 {
    return nauticalMiles * 2025.373;
}

// Picometers to Yards
pub fn picometersToYards(picometers: f64) f64 {
    return picometers * 0.000000000001093613;
}