// Micrometer Conversion

// Centimeters to Micrometers
double centimetersToMicrometers(double centimeters) {
  return centimeters * 10000;
}

// Decameters to Micrometers
double decametersToMicrometers(double decameters) {
  return decameters * 10000000;
}

// Decimeters to Micrometers
double decimetersToMicrometers(double decimeters) {
  return decimeters * 100000;
}

