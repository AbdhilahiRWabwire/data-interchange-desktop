// Yard Conversion

// Centimeters to Yards
double centimetersToYards(double centimeters) {
  return centimeters * 0.01093613;
}

// Decameters to Yards
double decametersToYards(double decameters) {
  return decameters * 10.93613;
}

// Decimeters to Yards
double decimetersToYards(double decimeters) {
  return decimeters * 0.1093613;
}

// Feet to Yards
double feetToYards(double feet) {
  return feet * 0.3333333;
}

// Hectometers to Yards
double hectometersToYards(double hectometers) {
  return hectometers * 109.3613;
}

// Inches to Yards
double inchesToYards(double inches) {
  return inches * 0.02777778;
}

// Kilometers to Yards
double kilometersToYards(double kilometers) {
  return kilometers * 1093.613;
}

// Meters to Yards
double metersToYards(double meters) {
  return meters * 1.093613;
}

// Micrometers to Yards
double micrometersToYards(double micrometers) {
  return micrometers * 0.000001093613;
}

// Miles to Yards
double milesToYards(double miles) {
  return miles * 1760;
}

// Millimeters to Yards
double millimetersToYards(double millimeters) {
  return millimeters * 0.001093613;
}

// Nanometers to Yards
double nanometersToYards(double nanometers) {
  return nanometers * 0.000000001093613;
}

// Nautical Miles to Yards
double nauticalMilesToYards(double nauticalMiles) {
  return nauticalMiles * 2025.373;
}

// Picometers to Yards
double picometersToYards(double picometers) {
  return picometers * 0.000000000001093613;
}
