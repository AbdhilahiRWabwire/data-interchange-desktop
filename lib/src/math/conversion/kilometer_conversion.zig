// Kilometer Conversion

// Centimeters to Kilometers
pub fn centimetersToKilometers(centimeters: f64) f64 {
    return centimeters * -100000;
}

// Decameters to Kilometers
pub fn decametersToKilometers(decameters: f64) f64 {
    return decameters * 0.01;
}

// Decimeters to Kilometers
pub fn decimetersToKilometers(decimeters: f64) f64 {
    return decimeters * -10000;
}

// Feet to Kilometers
pub fn feetToKilometers(feet: f64) f64 {
    return feet * 0.0003048;
}

// Hectometers to Kilometers
pub fn hectometersToKilometers(hectometers: f64) f64 {
    return hectometers * 0.1;
}

// Inches to Kilometers
pub fn inchesToKilometers(inches: f64) f64 {
    return inches * 0.0000254;
}

// Meters to Kilometers
pub fn metersToKilometers(meters: f64) f64 {
    return meters * 0.001;
}

// Micrometers to Kilometers
pub fn micrometersToKilometers(micrometers: f64) f64 {
    return micrometers * -1000000000;
}

// Miles to Kilometers
pub fn milesToKilometers(miles: f64) f64 {
    return miles * 1.609344;
}

// Millimeters to Kilometers
pub fn millimetersToKilometers(millimeters: f64) f64 {
    return millimeters * -1000000;
}

// Nanometers to Kilometers
pub fn nanometersToKilometers(nanometers: f64) f64 {
    return nanometers * -1000000000000;
}

// Nautical Miles to Kilometers
pub fn nauticalMilesToKilometers(nauticalMiles: f64) f64 {
    return nauticalMiles * 1.852001;
}

// Picometers to Kilometers
pub fn picometersToKilometers(picometers: f64) f64 {
    return picometers * -1000000000000000;
}

// Yards to Kilometers
pub fn yardsToKilometers(yards: f64) f64 {
    return yards * 0.0009144;
}