// Feet Conversion

// Centimeters to Feet
pub fn centimetersToFeet(centimeters: f64) f64 {
    return centimeters * 0.0328084;
}

// Decameters to Feet
pub fn decametersToFeet(decameters: f64) f64 {
    return decameters * 32.8084;
}

// Decimeters to Feet
pub fn decimetersToFeet(decimeters: f64) f64 {
    return decimeters * 0.328084;
}

// Hectometers to Feet
pub fn hectometersToFeet(hectometers: f64) f64 {
    return hectometers * 328.084;
}

// Inches to Feet
pub fn inchesToFeet(inches: f64) f64 {
    return inches * 0.08333333;
}

// Kilometers to Feet
pub fn kilometersToFeet(kilometers: f64) f64 {
    return kilometers * 3280.84;
}

// Meters to Feet
pub fn metersToFeet(meters: f64) f64 {
    return meters * 3.28084;
}

// Micrometers to Feet
pub fn micrometersToFeet(micrometers: f64) f64 {
    return micrometers * 0.00000328084;
}

// Miles to Feet
pub fn milesToFeet(miles: f64) f64 {
    return miles * 5280;
}

// Millimeters to Feet
pub fn millimetersToFeet(millimeters: f64) f64 {
    return millimeters * 0.00328084;
}

// Nanometers to Feet
pub fn nanometersToFeet(nanometers: f64) f64 {
    return nanometers * 0.00000000328084;
}

// Nautical Miles to Feet
pub fn nauticalMilesToFeet(nauticalMiles: f64) f64 {
    return nauticalMiles * 6076.118;
}

// Picometers to Feet
pub fn picometersToFeet(picometers: f64) f64 {
    return picometers * 0.00000000000328084;
}

// Yards to Feet
pub fn yardsToFeet(yards: f64) f64 {
    return yards * 3;
}