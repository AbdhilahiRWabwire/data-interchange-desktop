// Nanometer Conversion

// Centimeters to Nanometers
pub fn centimetersToNanometers(centimeters: f64) f64 {
    return centimeters * 10000000;
}

// Decameters to Nanometers
pub fn decametersToNanometers(decameters: f64) f64 {
    return decameters * 10000000000;
}

// Decimeters to Nanometers
pub fn decimetersToNanometers(decimeters: f64) f64 {
    return decimeters * 100000000;
}

// Feet to Nanometers
pub fn feetToNanometers(feet: f64) f64 {
    return feet * 304800000;
}

// Hectometers to Nanometers
pub fn hectometersToNanometers(hectometers: f64) f64 {
    return hectometers * 100000000000;
}

// Inches to Nanometers
pub fn inchesToNanometers(inches: f64) f64 {
    return inches * 25400000;
}

// Kilometers to Nanometers
pub fn kilometersToNanometers(kilometers: f64) f64 {
    return kilometers * 1000000000000;
}

// Meters to Nanometers
pub fn metersToNanometers(meters: f64) f64 {
    return meters * 1000000000;
}

// Micrometers to Nanometers
pub fn micrometersToNanometers(micrometers: f64) f64 {
    return micrometers * 1000;
}

// Miles to Nanometers
pub fn milesToNanometers(miles: f64) f64 {
    return miles * 1609344000000;
}

// Millimeters to Nanometers
pub fn millimetersToNanometers(millimeters: f64) f64 {
    return millimeters * 1000000;
}

// Nautical Miles to Nanometers
pub fn nauticalMilesToNanometers(nauticalMiles: f64) f64 {
    return nauticalMiles * 1852001000000;
}

// Picometers to Nanometers
pub fn picometersToNanometers(picometers: f64) f64 {
    return picometers * 0.001;
}

// Yards to Nanometers
pub fn yardsToNanometers(yards: f64) f64 {
    return yards * 914400000;
}