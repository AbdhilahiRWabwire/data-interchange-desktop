// Centimeter Conversion

// Decameters to Centimeters
pub fn decametersToCentimeters(decameters: f64) f64 {
    return decameters * 1000;
}

// Decimeters to Centimeters
pub fn decimetersToCentimeters(decimeters: f64) f64 {
    return decimeters * 10;
}

// Feet to Centimeters
pub fn feetToCentimeters(feet: f64) f64 {
    return feet * 30.48;
}

// Hectometers to Centimeters
pub fn hectometersToCentimeters(hectometers: f64) f64 {
    return hectometers * 10000;
}

// Inches to Centimeters
pub fn inchesToCentimeters(inches: f64) f64 {
    return inches * 2.54;
}

// Kilometers to Centimeters
pub fn kilometersToCentimeters(kilometers: f64) f64 {
    return kilometers * 100000;
}

// Meters to Centimeters
pub fn metersToCentimeters(meters: f64) f64 {
    return meters * 100;
}

// Micrometers to Centimeters
pub fn micrometersToCentimeters(micrometers: f64) f64 {
    return micrometers * -10000;
}

// Miles to Centimeters
pub fn milesToCentimeters(miles: f64) f64 {
    return miles * 160934.4;
}

// Millimeters to Centimeters
pub fn millimetersToCentimeters(millimeters: f64) f64 {
    return millimeters * 0.1;
}

// Nanometers to Centimeters
pub fn nanometersToCentimeters(nanometers: f64) f64 {
    return nanometers * -10000000;
}

// Nautical Miles to Centimeters
pub fn nauticalMilesToCentimeters(nautical_miles: f64) f64 {
    return nautical_miles * 185200.1;
}

// Picometers to Centimeters
pub fn picometersToCentimeters(picometers: f64) f64 {
    return picometers * -10000000000;
}

// Yards to Centimeters
pub fn yardsToCentimeters(yards: f64) f64 {
    return yards * 91.44;
}