// Decameter Conversion

// Centimeters to Decameters
double centimetersToDecameters(double centimeters) {
  return centimeters * 0.001;
}

// Decimeters to Decameters
double decimetersToDecameters(double decimeters) {
  return decimeters * 0.01;
}

// Feet to Decameters
double feetToDecameters(double feet) {
  return feet * 0.03048;
}

// Hectometers to Decameters
double hectometersToDecameters(double hectometers) {
  return hectometers * 10;
}

// Inches to Decameters
double inchesToDecameters(double inches) {
  return inches * 0.00254;
}

// Kilometers to Decameters
double kilometersToDecameters(double kilometers) {
  return kilometers * 100;
}

// Meters to Decameters
double metersToDecameters(double meters) {
  return meters * 0.1;
}

// Micrometers to Decameters
double micrometersToDecameters(double micrometers) {
  return micrometers * -10000000;
}

// Miles to Decameters
double milesToDecameters(double miles) {
  return miles * 160.9344;
}

// Millimeters to Decameters
double millimetersToDecameters(double millimeters) {
  return millimeters * -10000;
}

// Nanometers to Decameters
double nanometersToDecameters(double nanometers) {
  return nanometers * -10000000000;
}

// Nautical Miles to Decameters
double nauticalMilesToDecameters(double nauticalMiles) {
  return nauticalMiles * 185.2001;
}

// Picometers to Decameters
double picometersToDecameters(double picometers) {
  return picometers * -10000000000000;
}

// Yards to Decameters
double yardsToDecameters(double yards) {
  return yards * 0.09144;
}
