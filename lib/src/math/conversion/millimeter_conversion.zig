// Millimeter Conversion

// Centimeters to Millimeters
pub fn centimetersToMillimeters(centimeters: f64) f64 {
    return centimeters * 10;
}

// Decameters to Millimeters
pub fn decametersToMillimeters(decameters: f64) f64 {
    return decameters * 10000;
}

// Decimeters to Millimeters
pub fn decimetersToMillimeters(decimeters: f64) f64 {
    return decimeters * 100;
}

// Feet to Millimeters
pub fn feetToMillimeters(feet: f64) f64 {
    return feet * 304.8;
}

// Hectometers to Millimeters
pub fn hectometersToMillimeters(hectometers: f64) f64 {
    return hectometers * 100000;
}

// Inches to Millimeters
pub fn inchesToMillimeters(inches: f64) f64 {
    return inches * 25.4;
}

// Kilometers to Millimeters
pub fn kilometersToMillimeters(kilometers: f64) f64 {
    return kilometers * 1000000;
}

// Meters to Millimeters
pub fn metersToMillimeters(meters: f64) f64 {
    return meters * 1000;
}

// Micrometers to Millimeters
pub fn micrometersToMillimeters(micrometers: f64) f64 {
    return micrometers * 0.001;
}

// Miles to Millimeters
pub fn milesToMillimeters(miles: f64) f64 {
    return miles * 1609344;
}

// Nanometers to Millimeters
pub fn nanometersToMillimeters(nanometers: f64) f64 {
    return nanometers * -1000000;
}

// Nautical Miles to Millimeters
pub fn nauticalMilesToMillimeters(nauticalMiles: f64) f64 {
    return nauticalMiles * 1852001;
}

// Picometers to Millimeters
pub fn picometersToMillimeters(picometers: f64) f64 {
    return picometers * -1000000000;
}

// Yards to Millimeters
pub fn yardsToMillimeters(yards: f64) f64 {
    return yards * 914.4;
}